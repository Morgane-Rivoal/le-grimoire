const supportedLocales = Object.keys(translations);
let currentLocale = localStorage.getItem("grimoire-locale") || navigator.language?.slice(0, 2) || "fr";
if(!supportedLocales.includes(currentLocale)) currentLocale = "fr";

function t(key, variables = {}){
  const template = translations[currentLocale]?.[key] ?? translations.fr[key] ?? key;
  return Object.entries(variables).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    template
  );
}

function translateDocument(){
  document.documentElement.lang = currentLocale;
  document.title = t("app.title");
  document.querySelectorAll("[data-i18n]").forEach(element => {
    element.textContent = t(element.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(element => {
    element.placeholder = t(element.dataset.i18nPlaceholder);
  });
  document.querySelectorAll("[data-i18n-aria-label]").forEach(element => {
    element.setAttribute("aria-label", t(element.dataset.i18nAriaLabel));
  });
  const localeSelect = document.getElementById("localeSelect");
  if(localeSelect) localeSelect.value = currentLocale;
}

function setLocale(locale){
  if(!supportedLocales.includes(locale)) return;
  currentLocale = locale;
  localStorage.setItem("grimoire-locale", locale);
  translateDocument();
  if(typeof renderPlants === "function") renderPlants();
  if(typeof renderCollection === "function") renderCollection();
  if(typeof refreshCurrentPlantView === "function") refreshCurrentPlantView();
}
